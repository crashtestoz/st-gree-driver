-- Rcon (Round Constant)
local rcon = {
  0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
}

-- Galois Field multiplication
local function gmul(a, b)
  local p = 0
  for i = 0, 7 do
    if (b & 1) ~= 0 then
      p = p ~ a
    end
    local hi_bit_set = (a & 0x80) ~= 0
    a = (a << 1) & 0xFF
    if hi_bit_set then
      a = a ~ 0x1b
    end
    b = b >> 1
  end
  return p
end

-- Key expansion for AES-128
local function expand_key(key)
  local w = {}
  
  -- First 4 words are the key itself
  for i = 0, 3 do
    w[i] = (key:byte(i*4+1) << 24) | (key:byte(i*4+2) << 16) | (key:byte(i*4+3) << 8) | key:byte(i*4+4)
  end
  
  -- Expand to 44 words (11 round keys * 4 words)
  for i = 4, 43 do
    local temp = w[i-1]
    if i % 4 == 0 then
      -- RotWord
      temp = ((temp << 8) | (temp >> 24)) & 0xFFFFFFFF
      -- SubWord
      local b0 = sbox[((temp >> 24) & 0xFF) + 1]
      local b1 = sbox[((temp >> 16) & 0xFF) + 1]
      local b2 = sbox[((temp >> 8) & 0xFF) + 1]
      local b3 = sbox[(temp & 0xFF) + 1]
      temp = (b0 << 24) | (b1 << 16) | (b2 << 8) | b3
      -- XOR with Rcon
      temp = temp ~ (rcon[(i // 4)] << 24)
    end
    w[i] = w[i-4] ~ temp
  end
  
  return w
end

-- SubBytes transformation
local function sub_bytes(state)
  for i = 0, 15 do
    state[i] = sbox[state[i] + 1]
  end
end

-- InvSubBytes transformation
local function inv_sub_bytes(state)
  for i = 0, 15 do
    state[i] = inv_sbox[state[i] + 1]
  end
end

-- ShiftRows transformation
local function shift_rows(state)
  local temp
  
  -- Row 1: shift left by 1
  temp = state[1]
  state[1] = state[5]
  state[5] = state[9]
  state[9] = state[13]
  state[13] = temp
  
  -- Row 2: shift left by 2
  temp = state[2]
  state[2] = state[10]
  state[10] = temp
  temp = state[6]
  state[6] = state[14]
  state[14] = temp
  
  -- Row 3: shift left by 3 (or right by 1)
  temp = state[15]
  state[15] = state[11]
  state[11] = state[7]
  state[7] = state[3]
  state[3] = temp
end

-- InvShiftRows transformation
local function inv_shift_rows(state)
  local temp
  
  -- Row 1: shift right by 1
  temp = state[13]
  state[13] = state[9]
  state[9] = state[5]
  state[5] = state[1]
  state[1] = temp
  
  -- Row 2: shift right by 2
  temp = state[2]
  state[2] = state[10]
  state[10] = temp
  temp = state[6]
  state[6] = state[14]
  state[14] = temp
  
  -- Row 3: shift right by 3 (or left by 1)
  temp = state[3]
  state[3] = state[7]
  state[7] = state[11]
  state[11] = state[15]
  state[15] = temp
end

-- MixColumns transformation
local function mix_columns(state)
  for i = 0, 3 do
    local s0 = state[i*4]
    local s1 = state[i*4+1]
    local s2 = state[i*4+2]
    local s3 = state[i*4+3]
    
    state[i*4] = gmul(s0, 2) ~ gmul(s1, 3) ~ s2 ~ s3
    state[i*4+1] = s0 ~ gmul(s1, 2) ~ gmul(s2, 3) ~ s3
    state[i*4+2] = s0 ~ s1 ~ gmul(s2, 2) ~ gmul(s3, 3)
    state[i*4+3] = gmul(s0, 3) ~ s1 ~ s2 ~ gmul(s3, 2)
  end
end

-- InvMixColumns transformation
local function inv_mix_columns(state)
  for i = 0, 3 do
    local s0 = state[i*4]
    local s1 = state[i*4+1]
    local s2 = state[i*4+2]
    local s3 = state[i*4+3]
    
    state[i*4] = gmul(s0, 14) ~ gmul(s1, 11) ~ gmul(s2, 13) ~ gmul(s3, 9)
    state[i*4+1] = gmul(s0, 9) ~ gmul(s1, 14) ~ gmul(s2, 11) ~ gmul(s3, 13)
    state[i*4+2] = gmul(s0, 13) ~ gmul(s1, 9) ~ gmul(s2, 14) ~ gmul(s3, 11)
    state[i*4+3] = gmul(s0, 11) ~ gmul(s1, 13) ~ gmul(s2, 9) ~ gmul(s3, 14)
  end
end

-- AddRoundKey transformation
local function add_round_key(state, round_key, round)
  for i = 0, 3 do
    local k = round_key[round*4 + i]
    state[i*4] = state[i*4] ~ ((k >> 24) & 0xFF)
    state[i*4+1] = state[i*4+1] ~ ((k >> 16) & 0xFF)
    state[i*4+2] = state[i*4+2] ~ ((k >> 8) & 0xFF)
    state[i*4+3] = state[i*4+3] ~ (k & 0xFF)
  end
end

-- Encrypt single block (16 bytes)
function aes128.encrypt_block(block, key_schedule)
  local state = {}
  
  -- Convert block to state array (column-major order)
  for i = 0, 15 do
    state[i] = block:byte(i+1)
  end
  
  -- Initial round key addition
  add_round_key(state, key_schedule, 0)
  
  -- 9 main rounds
  for round = 1, 9 do
    sub_bytes(state)
    shift_rows(state)
    mix_columns(state)
    add_round_key(state, key_schedule, round)
  end
  
  -- Final round (no MixColumns)
  sub_bytes(state)
  shift_rows(state)
  add_round_key(state, key_schedule, 10)
  
  -- Convert state back to string
  local result = {}
  for i = 0, 15 do
    result[i+1] = string.char(state[i])
  end
  
  return table.concat(result)
end

-- Decrypt single block (16 bytes)
function aes128.decrypt_block(block, key_schedule)
  local state = {}
  
  -- Convert block to state array (column-major order)
  for i = 0, 15 do
    state[i] = block:byte(i+1)
  end
  
  -- Initial round key addition
  add_round_key(state, key_schedule, 10)
  
  -- 9 main rounds
  for round = 9, 1, -1 do
    inv_shift_rows(state)
    inv_sub_bytes(state)
    add_round_key(state, key_schedule, round)
    inv_mix_columns(state)
  end
  
  -- Final round (no InvMixColumns)
  inv_shift_rows(state)
  inv_sub_bytes(state)
  add_round_key(state, key_schedule, 0)
  
  -- Convert state back to string
  local result = {}
  for i = 0, 15 do
    result[i+1] = string.char(state[i])
  end
  
  return table.concat(result)
end

-- ECB mode encryption
function aes128.encrypt_ecb(plaintext, key)
  if #key ~= 16 then
    error("AES-128 requires 16-byte key, got " .. #key)
  end
  
  if #plaintext % 16 ~= 0 then
    error("Plaintext length must be multiple of 16, got " .. #plaintext)
  end
  
  -- Expand key
  local key_schedule = expand_key(key)
  
  -- Encrypt each block
  local result = {}
  for i = 1, #plaintext, 16 do
    local block = plaintext:sub(i, i+15)
    result[#result+1] = aes128.encrypt_block(block, key_schedule)
  end
  
  return table.concat(result)
end

-- ECB mode decryption
function aes128.decrypt_ecb(ciphertext, key)
  if #key ~= 16 then
    error("AES-128 requires 16-byte key, got " .. #key)
  end
  
  if #ciphertext % 16 ~= 0 then
    error("Ciphertext length must be multiple of 16, got " .. #ciphertext)
  end
  
  -- Expand key
  local key_schedule = expand_key(key)
  
  -- Decrypt each block
  local result = {}
  for i = 1, #ciphertext, 16 do
    local block = ciphertext:sub(i, i+15)
    result[#result+1] = aes128.decrypt_block(block, key_schedule)
  end
  
  return table.concat(result)
end

return aes128
